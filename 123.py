# Описание переменных и структур данных
# Инициализация пустого списка
a = []  # Это пустой список для хранения последовательности элементов любого типа

# Список представляет собой упорядоченную изменяемую коллекцию элементов
# Элементы могут быть любого типа: числа, строки, другие списки
# Списки позволяют хранить данные с возможностью добавления, удаления и изменения элементов

# Инициализация стека
m = []  # Это стек, реализованный на основе списка

# Стек - структура данных типа LIFO (Last In, First Out)
# Последний добавленный элемент будет извлечен первым
# Основные операции: добавление (push) и извлечение (pop) элементов

# Добавление элементов в стек с помощью функции append()
m.append(10) 
m.append(20)  
m.append(30) 

# Стек теперь содержит: [10, 20, 30]
