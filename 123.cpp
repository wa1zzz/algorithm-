// Описание переменных и структур данных

#include <iostream>
#include <vector>
#include <stack>
using namespace std;

int main() {
    // Инициализация пустого вектора
    vector<int> a;  // Это пустой вектор для хранения целых чисел
    
    // Вектор представляет собой динамический массив с автоматическим управлением памятью
    // Поддерживает произвольный доступ к элементам и различные операции
    // Инициализация стека
    stack<int> m;  // Это стек для хранения целых чисел
    // Стек - структура данных типа LIFO (Last In, First Out)
    // Последний добавленный элемент будет извлечен первым
    // Основные операции: push (добавление) и pop (извлечение)
    // Добавление элементов в стек с помощью метода push()
    m.push(10);    // Добавление элемента 10 в стек
    m.push(20);    // Добавление элемента 20 в стек
    m.push(30);    // Добавление элемента 30 в стек
    // Стек теперь содержит: 10, 20, 30 (сверху вниз)
    return 0;
}
